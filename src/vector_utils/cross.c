#include "vector.h"

/// @brief do cross product between v1 and v2
/// @param v1
/// @param v2
/// @return t_v4sd 
t_v4sd	cross(t_v4sd v1, t_v4sd v2)
{
	return ((t_v4sd){
		v1[1] * v2[2] - v2[2] * v1[1],
		v1[2] * v2[0] - v2[0] * v1[2],
		v1[0] * v2[1] - v2[1] * v1[0]
	});
}
