#include "vector.h"

t_v4si	cross(t_v4si v1, t_v4si v2)
{
	return ((t_v4si){
		v1[1] * v2[2] - v2[2] * v1[1],
		v1[2] * v2[0] - v2[0] * v1[2],
		v1[0] * v2[1] - v2[1] * v1[0]
	});
}
