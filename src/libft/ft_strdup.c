#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	len;

	len = ft_strlen(s1);
	s2 = malloc(len + 1);
	if (s2 != NULL)
		ft_memcpy(s2, s1, len + 1);
	return (s2);
}
